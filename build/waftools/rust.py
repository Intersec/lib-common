@TaskGen.feature('rust')
@TaskGen.before_method('process_use')
def rust_create_task(self: TaskGen) -> None:
    ctx = self.bld

    cargo_packages = getattr(ctx, 'cargo_packages', None)
    if cargo_packages is None:
        # We are not really in the build stage, do nothing.
        return

    # Get the waf target name
    waf_target_name = self.name

    # Get the cargo package name to use
    cargo_pkg_name = getattr(self, 'cargo_package', waf_target_name)

    # Get the cargo package corresponding to the waf target
    pkg_metadata = cargo_packages.get(cargo_pkg_name)
    if pkg_metadata is None:
        ctx.fatal(f'waf target `{cargo_pkg_name}` does not correspond to a '
                  'cargo package')

    # Get manifest path and package dir
    manifest_path = pkg_metadata['manifest_path']
    package_dir = osp.dirname(manifest_path)

    use_pic = self.name.endswith('.pic')

    # Generate the profile variables depending on 'pic' use.
    target_profile_suffix: str = ''
    if use_pic:
        target_profile_suffix = '-pic'
    cargo_profile = ctx.env.CARGO_PROFILE + target_profile_suffix

    # Special case for debug + pic.
    # In that case, the 'non-pic' build dir is 'debug' (like the waf profile),
    # but the 'pic' build dir is 'dev-pic' (cargo profile + '-pic')
    cargo_build_dir: str = self.env.CARGO_BUILD_DIR
    if use_pic and ctx.env.PROFILE == 'debug':
        cargo_build_dir = osp.join(osp.dirname(cargo_build_dir), 'dev')

    # Special case for sanitizers + not pic.
    # In that case, the target build is located in a
    # 'x86_64-unknown-linux-gnu' sub directory
    if not use_pic and ctx.env.USE_SANITIZER:
        profile_dir = osp.basename(cargo_build_dir)
        target_dir = osp.dirname(cargo_build_dir)
        cargo_build_dir = osp.join(target_dir, 'x86_64-unknown-linux-gnu',
                                   profile_dir)

    cargo_bld_name = cargo_build_dir + target_profile_suffix
    cargo_bld_dir = ctx.srcnode.make_node(cargo_bld_name)

    # Build the list of outputs and hardlinks for the task
    outputs: list[Node] = []
    hardlinks: list[tuple[str, str]] = []
    task_kind = 'CargoBuildLinkTask'

    # For each cargo target, get the task to create and populate the outputs
    # and hardlinks
    for cargo_target in pkg_metadata['targets']:
        cargo_target_name = cargo_target['name']
        kinds = cargo_target['kind']

        # Add the rust libs compiled as a static lib
        if 'staticlib' in kinds:
            cargo_target_output_name = (
                ctx.env.cstlib_PATTERN % cargo_target_name
            )
            cargo_output = cargo_bld_dir.make_node(cargo_target_output_name)

            waf_target_output_name = (
                ctx.env.cstlib_PATTERN % waf_target_name
            )

            # Hard-link the rust static lib to waf build directory
            waf_relative_src_path = self.path.path_from(ctx.srcnode)
            waf_output_dir = ctx.bldnode.make_node(waf_relative_src_path)
            waf_output_dir.mkdir()
            waf_output = waf_output_dir.make_node(waf_target_output_name)

            outputs.extend([waf_output, cargo_output])
            hardlinks.append((waf_output, cargo_output))

            task_kind = 'CargoBuildStlinkTask'

        # Add the rust libs compiled as a shared lib
        if 'cdylib' in kinds:
            cargo_target_output_name = (
                ctx.env.cshlib_PATTERN % cargo_target_name
            )
            cargo_output = cargo_bld_dir.make_node(cargo_target_output_name)

            waf_target_output_name = (
                ctx.env.cshlib_PATTERN % waf_target_name
            )
            if not getattr(self, 'keep_lib_prefix', False):
                # Remove the lib prefix unless specified otherwise
                assert waf_target_output_name.startswith('lib')
                waf_target_output_name = waf_target_output_name[len('lib'):]
            waf_output = self.path.make_node(waf_target_output_name)

            outputs.extend([waf_output, cargo_output])
            hardlinks.append((waf_output, cargo_output))

        # Add the rust bin
        if 'bin' in kinds:
            # Use the same target output name for bin as there can be multiple
            # bins in a Cargo package.
            target_output_name = (
                ctx.env.cprogram_PATTERN % cargo_target_name
            )

            cargo_output = cargo_bld_dir.make_node(target_output_name)
            waf_output = self.path.make_node(target_output_name)

            outputs.extend([waf_output, cargo_output])
            hardlinks.append((waf_output, cargo_output))

        # Ignore rust lib, rlib, dylib and proc-macro

    # `link_task` is required for use lib links in waf.
    self.link_task = self.rust_task = tsk = self.create_task(
        task_kind, [ctx.root.make_node(manifest_path)], outputs)
    tsk.env.CARGO_PROFILE = cargo_profile
    tsk.env.PKG_DIR = package_dir
    tsk.env.PKG_NAME = cargo_pkg_name
    tsk.env.PKG_HARDLINKS = hardlinks
    tsk.env.PKG_PROFILE_SUFFIX = target_profile_suffix
    tsk.env.USE_PIC = use_pic


@TaskGen.feature('rust')
@TaskGen.after_method('process_use')
def rust_add_dep_task(self: TaskGen) -> None:
    """
    Add task dependencies between rust task gen even if the waf rust task gen
    does not produce an output.
    """
    ctx = self.bld

    # Get the link task
    link_task = getattr(self, 'link_task', None)
    if link_task is None:
        # We are not really in the build stage, do nothing.
        return

    if not link_task.outputs:
        # If the task do not produce any output (rust lib), delete `link_task`
        # to avoid a waf exception later on with some invalid ouputs.
        del self.link_task

    rust_task = self.rust_task

    for use_stlib in self.tmp_use_sorted:  # Result of sorted use libs
        dep_tg = ctx.get_tgen_by_name(use_stlib)
        dep_rust_task = getattr(dep_tg, 'rust_task', None)
        if dep_rust_task is None:
            # Not a rust task generator
            continue

        self.rust_task.set_run_after(dep_rust_task)

        # If the rust task dependencies does not have any outputs, it is a
        # pure cargo target, do nothing more
        if not dep_rust_task.outputs:
            continue

        # The rust dependencies need to be linked by cargo and not by waf.
        # So remove them from STLIB and LIB in the task env.
        dep_target_name = dep_tg.target
        dep_output = dep_rust_task.outputs[0]
        dep_output_dir_path = dep_output.parent.srcpath()

        remove_in_list_no_err(rust_task.env.STLIB, dep_target_name)
        remove_in_list_no_err(rust_task.env.LIB, dep_target_name)

        remove_in_list_no_err(rust_task.env.STLIBPATH, dep_output_dir_path)
        remove_in_list_no_err(rust_task.env.LIBPATH, dep_output_dir_path)


