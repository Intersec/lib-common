def get_libcommon_node(ctx: BuildContext) -> Node:
    """Get the lib-common path node."""
    file_realpath = osp.realpath(__file__)
    file_node = ctx.root.make_node(file_realpath)
    return file_node.parent.parent.parent


def generate_deps_workspace_hack_cargo_toml(ctx: BuildContext) -> None:
    """
    Generate the deps-workspace-hack/Cargo.toml from the workspace
    dependencies.

    In order to avoid compiling the same dependencies crate with some
    feature differences because of dependency hierarchy, use a crate
    `deps-workspace-hack` that list all the workspace dependencies and is
    always used when running `cargo build` by waf.

    This does not mean that in the result compilation binaries there will be
    all the dependencies of the workspace. Only the dependencies of the
    crate are still used for a given crate. But the dependencies will use
    all the features specified in the workspaces.
    """
    # Read the manifest root Cargo.toml
    libcommon_node = get_libcommon_node(ctx)
    deps_workspace_hack_node = libcommon_node.make_node(
        'rust/deps-workspace-hack')
    manifest_node = ctx.srcnode.make_node('Cargo.toml')

    with open(manifest_node.abspath(), 'rb') as f:
        manifest_content_val = tomllib.load(f)

    # Extract the dependencies
    dependencies = manifest_content_val['workspace']['dependencies']
    dependencies_names = dependencies.keys()
    dependencies_names_str = '\n'.join(
        f'{dep_name}.workspace = true' for dep_name in dependencies_names
    )

    # Read the template
    cargo_tpl_node = deps_workspace_hack_node.make_node('Cargo.toml.tpl')
    cargo_tpl_content = cargo_tpl_node.read()

    cargo_new_content = cargo_tpl_content

    # Replace header
    new_header = """
# THIS FILE IS AUTOGENERATED DO NOT MODIFY DIRECTLY

""".lstrip()
    package_idx = cargo_new_content.find('[package]')
    cargo_new_content = new_header + cargo_new_content[package_idx:]

    # Add dependency names
    cargo_new_content = cargo_new_content.replace(
        '$WORKSPACE_DEPS', dependencies_names_str)

    # Write the new content
    cargo_path = deps_workspace_hack_node.make_node('Cargo.toml')
    cargo_path.write(cargo_new_content)


